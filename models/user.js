function User(id, username, password, fullname, email, images, language, type, description) {
    this.id = id;
    this.username = username;
    this.password = password;
    this.fullname = fullname;
    this.email = email;
    this.images = images;
    this.language = language;
    this.type = type;
    this.description = description;
}
